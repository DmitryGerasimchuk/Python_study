def dict_to_list(d):
    my_answer = []
    for k, v in d.items():
        # 2. Если значение ключа - целое число, то его нужно умножить на 2 перед добавлением в кортеж.
        if isinstance(v, int):
            v *= 2
        my_answer.append((k, v))
    return my_answer


